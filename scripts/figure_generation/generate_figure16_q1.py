#!/usr/bin/env python3
"""
Figure 16 — Knee Fidelity Metrics
Q1 journal quality (7.0 in wide × 6.5 in tall, 2 panels vertically stacked).

Layout: 2 rows × 1 column
  (a) ψ₅₀ boxplots — suction at S_e = 0.5 (log y-axis)
  (b) Max-slope boxplots — steepest |Δθ/Δlog₁₀ψ| along the curve

Models shown: Observed | Gradient Boosting | MonotonicPINN | VGParamNet

Data source: pre-computed CSV at
  results_pinn_fixed/knee_fidelity/knee_fidelity_results.csv
  (generated by analysis/knee_fidelity_analysis.py — no retraining needed)

Design (matches Figures 13–15)
------
* 7.0 in wide × 6.5 in tall (2 × 3.0 in rows + spacing)
* Arial 12 pt: axis labels, tick labels, panel tags
* Arial 10 pt: legend
* Colors: Observed = #000000, GB = #1F77B4, PINN = #D62728, VGParamNet = #9B59B6
* Panel tags outside top-left; no grid; inward ticks mirrored; clean box
* 600 dpi PNG (→ paper_figures/png/) + PDF (→ paper_figures/)
* pdf.fonttype = 42
"""

from __future__ import annotations
from pathlib import Path
import sys

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

ROOT    = Path(__file__).resolve().parents[2]
PNG_DIR = ROOT / "paper_figures" / "png"
PDF_DIR = ROOT / "paper_figures"
PNG_DIR.mkdir(parents=True, exist_ok=True)

FONT_MAIN  = 12
FONT_SMALL = 10
LW         = 1.8

matplotlib.rcParams.update({
    "text.usetex": False,
    "axes.formatter.use_mathtext": False,
    "font.family": "sans-serif",
    "font.sans-serif": ["Arial", "Helvetica", "DejaVu Sans", "Liberation Sans"],
    "font.size": FONT_MAIN,
    "axes.labelsize": FONT_MAIN,
    "xtick.labelsize": FONT_MAIN,
    "ytick.labelsize": FONT_MAIN,
    "legend.fontsize": FONT_SMALL,
    "axes.linewidth": 0.8,
    "xtick.major.width": 0.8,
    "ytick.major.width": 0.8,
    "xtick.major.size": 4,
    "ytick.major.size": 4,
    "xtick.direction": "in",
    "ytick.direction": "in",
    "pdf.fonttype": 42,
    "ps.fonttype": 42,
    "axes.unicode_minus": False,
})

# ── Model order and appearance ──────────────────────────────────────────────
METHODS    = ["Observed", "GB", "PINN", "VGParamNet"]
COLORS     = {
    "Observed":   "#000000",   # black
    "GB":         "#1F77B4",   # blue   (matches Figures 13–15)
    "PINN":       "#D62728",   # red    (matches Figures 13–15)
    "VGParamNet": "#9B59B6",   # purple
}
XLABELS    = ["Observed", "Gradient\nBoosting", "Monotonic\nPINN", "VGParamNet"]
POSITIONS  = [1, 2, 3, 4]


# ── Style helpers (identical to Figures 13–15) ───────────────────────────────
def _style(ax, *, log_y: bool = False):
    ax.set_facecolor("white")
    for sp in ax.spines.values():
        sp.set_linewidth(0.8)
        sp.set_color("black")
    ax.tick_params(which="both", top=True, right=True,
                   direction="in", labelsize=FONT_MAIN)
    ax.grid(False)
    for lbl in ax.get_xticklabels() + ax.get_yticklabels():
        lbl.set_fontfamily("Arial")
        lbl.set_fontsize(FONT_MAIN)


def _panel_tag(ax, tag):
    ax.text(0.0, 1.03, tag, transform=ax.transAxes,
            fontsize=FONT_MAIN, fontweight="normal", fontfamily="Arial",
            va="bottom", ha="left", clip_on=False)


def _boxplot_style(bp, methods):
    """Apply per-model colour and consistent line style to a patch_artist boxplot."""
    for patch, m in zip(bp["boxes"], methods):
        patch.set_facecolor(COLORS[m])
        patch.set_alpha(0.55)
        patch.set_linewidth(0.8)
    for elem in ["whiskers", "caps"]:
        for line in bp[elem]:
            line.set_linewidth(0.8)
            line.set_color("#444444")
    for med in bp["medians"]:
        med.set_color("black")
        med.set_linewidth(1.5)
    for fl in bp["fliers"]:
        fl.set_marker("o")
        fl.set_markersize(3)
        fl.set_markerfacecolor("none")
        fl.set_markeredgewidth(0.6)
        fl.set_markeredgecolor("#888888")


# ── Panel plotters ───────────────────────────────────────────────────────────
def _plot_panel_a(ax, data_by_method):
    """(a) ψ₅₀ — suction where S_e = 0.5 (knee location)."""
    arrays = [data_by_method[m]["psi_50"] for m in METHODS]

    bp = ax.boxplot(arrays,
                    positions=POSITIONS, widths=0.55,
                    patch_artist=True, showfliers=True,
                    medianprops=dict(color="black", linewidth=1.5),
                    whiskerprops=dict(linewidth=0.8),
                    capprops=dict(linewidth=0.8),
                    flierprops=dict(marker="o", markersize=3,
                                   markerfacecolor="none", markeredgewidth=0.6))
    _boxplot_style(bp, METHODS)

    ax.set_yscale("log")
    ax.set_xticks(POSITIONS)
    ax.set_xticklabels(XLABELS, fontsize=FONT_MAIN, fontfamily="Arial")
    ax.set_xlim(0.3, 4.7)
    ax.set_ylabel("Matric suction \u03c8 at Se = 0.5  (kPa)",
                  fontsize=FONT_MAIN, fontfamily="Arial", labelpad=6)

    # Minor log ticks
    ax.yaxis.set_minor_locator(matplotlib.ticker.LogLocator(
        base=10.0, subs=np.arange(2, 10) * 0.1, numticks=20))

    _style(ax, log_y=True)
    _panel_tag(ax, "(a) Knee location: suction at Se = 0.5")


def _plot_panel_b(ax, data_by_method):
    """(b) Max slope |Δθ/Δlog₁₀ψ| — knee sharpness."""
    arrays = [data_by_method[m]["max_slope"] for m in METHODS]

    bp = ax.boxplot(arrays,
                    positions=POSITIONS, widths=0.55,
                    patch_artist=True, showfliers=True,
                    medianprops=dict(color="black", linewidth=1.5),
                    whiskerprops=dict(linewidth=0.8),
                    capprops=dict(linewidth=0.8),
                    flierprops=dict(marker="o", markersize=3,
                                   markerfacecolor="none", markeredgewidth=0.6))
    _boxplot_style(bp, METHODS)

    ax.set_xticks(POSITIONS)
    ax.set_xticklabels(XLABELS, fontsize=FONT_MAIN, fontfamily="Arial")
    ax.set_xlim(0.3, 4.7)
    ax.set_ylabel("Max |\u0394\u03b8 / \u0394log10\u03c8|  [-]",
                  fontsize=FONT_MAIN, fontfamily="Arial", labelpad=6)

    _style(ax)
    _panel_tag(ax, "(b) Knee sharpness: steepest slope on log-suction SWCC")


# ── Main ─────────────────────────────────────────────────────────────────────
def main() -> int:
    import matplotlib.ticker

    csv_path = ROOT / "results_pinn_fixed" / "knee_fidelity" / "knee_fidelity_results.csv"
    if not csv_path.exists():
        print(f"ERROR: pre-computed CSV not found:\n  {csv_path}")
        print("Run:  python analysis/knee_fidelity_analysis.py  first.")
        return 1

    df = pd.read_csv(csv_path)
    print(f"Loaded {len(df)} rows from {csv_path.name}")

    # Check all methods are present
    missing = [m for m in METHODS if m not in df["method"].unique()]
    if missing:
        print(f"WARNING: methods not found in CSV: {missing}")
        print(f"  Available: {sorted(df['method'].unique())}")
        return 1

    data_by_method = {
        m: {
            "psi_50":    df.loc[df["method"] == m, "psi_50"].dropna().values,
            "max_slope": df.loc[df["method"] == m, "max_slope"].dropna().values,
        }
        for m in METHODS
    }

    # Print quick stats
    print("\n  Method            psi_50 median (kPa)   max_slope median")
    print("  " + "-" * 58)
    for m in METHODS:
        p50 = np.median(data_by_method[m]["psi_50"])
        ms  = np.median(data_by_method[m]["max_slope"])
        print(f"  {m:<18}  {p50:>18.2f}   {ms:>14.4f}")

    # ── Build figure: 2 rows × 1 column ──────────────────────────────────────
    FIG_W  = 7.0
    ROW_H  = 3.0
    HSPACE = 0.60

    fig, axes = plt.subplots(2, 1,
                             figsize=(FIG_W, ROW_H * 2),
                             gridspec_kw=dict(hspace=HSPACE))

    _plot_panel_a(axes[0], data_by_method)
    _plot_panel_b(axes[1], data_by_method)

    fig.align_ylabels(axes)
    fig.subplots_adjust(left=0.16, right=0.97, top=0.96, bottom=0.07)

    stem = "Figure16_Knee_Fidelity_Metrics_q1"
    pdf_out = PDF_DIR / f"{stem}.pdf"
    png_out = PNG_DIR / f"{stem}.png"
    fig.savefig(str(pdf_out), dpi=600, bbox_inches="tight", pad_inches=0.05)
    fig.savefig(str(png_out), dpi=600, bbox_inches="tight", pad_inches=0.05)
    plt.close(fig)
    print(f"\nSaved:\n  {pdf_out}\n  {png_out}")
    return 0


if __name__ == "__main__":
    # matplotlib.ticker needed inside main(); import at module level too
    import matplotlib.ticker
    raise SystemExit(main())
